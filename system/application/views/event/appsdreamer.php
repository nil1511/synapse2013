	<h2>Event Structure</h2>
	<div class="pane">
		<p>Apps Dreamer is a two stage event, in Phase 1 you are to submit an abstract. Those selected for Phase 2 should have a working application by the end of the Phase 2 submission date. Please also read the Rules.</p>

		<h3>Phase 1</h3>

		<p>Each team need to submit a detailed abstract of their idea. Abstract should also include technical requirements and a brief feasibility study of your idea.</p>

		<h3>Phase 2</h3>
		<p>Based on the given abstracts, individual <strong>team leaders</strong> will be sent an e-mail if they are selected for Phase 2. The list of selected teams will also be available on the website. Selected teams should send their working application by the Phase 2 deadline. Teams should send <strong>everything</strong> mentioned below. This should include:</p>

		<ol>
		<li>Application source code as a zip or tar.gz file.</li>
		<li>List of dependencies/extra software required for running this application (including their Licenses).</li>
		<li>special instruction to run the application(if any)</li>
		<li>A PDF containing user documentation. This could include a Quick Start Guide or a small tutorial if your application has some powerful or hard to understand feature.</li>
		<li>All of this should be sent in a <strong>.tar.gz or .zip file</strong> to <a href="mailto:appdreamer.daiict@gmail.com">appdreamer.daiict@gmail.com</a> with the Subject as <strong>&lt;TeamCode&gt;_Phase2</strong>.</li>
		</ol>
