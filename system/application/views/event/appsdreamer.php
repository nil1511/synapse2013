	<h2>Rules</h2>
	<div class="pane">
		<ul>
			<li>Each team can have 1-4 members.</li>
			<li>Number of teams selected for Phase 2 will be either <strong>20</strong> or <strong>floor( number of teams in Phase 1 * 0.2 )</strong>, whichever is <strong>smaller</strong>.</li>
			<li>All code must be distributed under an <a href="http://www.opensource.org/licenses/alphabetical">open source license</a>.</li>
			<li>Third-party/external libraries may be used, but they must be Open Source too.</li>
			<li>All participants must be students in a certified undergraduate or graduate institution.</li>
			<li>Each team may submit only *ONE* project.</li>
			<li>Teams are allowed to send in changes to both Phase 1 and Phase 2 submissions before the deadline. The latest submission will be considered for evaluation.</li>
			<li>Teams may change their ideas during Phase 1, but only before the deadline.</li>
			<li>No change in project will be considered once a team has been selected for Phase 2.</li>
			<li>Plagiarism will lead to immediate disqualification.</li>
			<li>The decisions of judges and/or event co-ordinators is final.</li>
			<li>Prize Money :<font color="white"> Rs.5000</font></li>
		</ul>
