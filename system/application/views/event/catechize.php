	<h2>Event Structure</h2>
	<div class="pane">
		<ul>
			<li>Team size - 2</li>

			<li>Number of Rounds : There would be 2 rounds. A sudden death round will also held in case of any deadlock situation between any team. </li>

			<li>Structure of each round : 

				<ul>
				<li>Round 1 : Will be conducted in paper-pen format. The teams are quizzed questions related to the basic understanding of networking. Based on the performance in this round, the selected teams would proceed to the next round.</li>

				<li>Round 2 - Will be conducted in our campus and will be of 1-hour duration.

			In this phase, selected teams would be grilled for their knowledge in core networking protocol and different kind of layers. 

			Each team would be analogous to a data packet. 
			There would be routers (volunteers) placed at several locations in the campus.

			Teams would begin from a particular 'starting' router. The aim would be to reach the destination router in the shortest possible time. At each router the team would be asked a question. If the team answers correctly, it would be told the next router address. Otherwise, it would be misrouted, i.e. given the wrong ?next router address?. The team which gives the most correct answers would obviously reach the destination first.</li>
				</ul>

			</li>

			<li>Method of elimination : A team will get eliminated in Round 2 if they give 3 consecutive incorrect answers or reach a hop count of 15.</li>

			<li>Appropriate venue : Round I can be held in any LT or in any CEP room depending upon the participation. For Round II there will routers be located throughout the campus. </li>

			<li>Method for deciding winning teams: First 2 Teams reaching the destination router will be declared as winners (1st team will be the one which reaches the destination router earliest.) In case 2 teams don't reach the destination router then those who reach closest to the destination router in the given time duration i.e. 1 hour will be declared as winners</li>

		</ul>
	</div>
	
