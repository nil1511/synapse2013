	<h2>Rules</h2>
	<div class="pane">
	Register individually; leave the group name on registration page empty.
	<br>Phase 1- Tambola:
	<ul>
<li>This is an individual event.
<li>The registered users would be divided into groups.
<li>The game would be played amongst the users of each group.
<li>The game would consist of 3 rounds of tambola.
<li>For each round a user will get 4 tickets.
<li>The Top scorer after the 3 rounds of Tambola (described below) wins the game .
<li>The winner of the game wins Rs 100 and qualifies for the main event
	</ul>
	<br>Rules of Tambola/Housie:
	<ul>	
<li>Housie ticket: In the first column you will see numbers from 1 to 9, in the second the numbers 10 to 19 appear, the third contains 20 to 29. This pattern continues until the final column (the eighth) which contains the numbers 80 to 90.
<li>A number will be called (shown on screen). If you have that number in any on your 4 tickets, mark them with a cross.
<li>Make a row. If the numbers called fill a row on your Tambola/Housie card, you can win if you are the first to claim it, buy pressing the SUBMIT button. 
<li>Like rows(Top row, bottom row and middle row, there are points for First Five and Full House.
<li>The highest point scorer wins the round. 
</ul>
<br>Phase 2- Cozener:
<ul>
<li>This game is based on a famous TV show Bluffmaster.
<li>A question having 10 answers would be asked in each round.
<li>The answers are ranked based on their uniqueness  from 10 to 100. The most unique answer gets 100 points and the least 10 points.
<li>A randomly selected participant in each round will have all the answers present on his screen.( HE IS THE BLUFFMASTER)
<li>After each round, the participants try to vote out the Bluffmaster.
<li>The participant getting the most votes leaves the game.  If he is not the BLUFFMASTER,  then the score of each participant is halved.
<li>In the last round, the voted out contestants and audience would vote the BLUFFMASTER.
</ul>
