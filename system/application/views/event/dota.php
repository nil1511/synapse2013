	<h2>Event Structure</h2>
	<div class="pane">
		<h3>Competition Method</h3>
		<ul>
		<li>5 vs. 5 (Team Play, 5 players per team)</li>
		<li>Clan can bring one extra player for substitution, but substitution can be made before the game starts. During the game, player substitution and exchange of seats will not be allowed.</li>
		<li>Matches are played according to the tournament structure and the schedule provided by the tournament officials. </li>
		<li>Map  to be played: DotA All-stars v6.73c or the latest stable map available.During  the game, warnings will be issued if Referee finds any abuse or fault. The team  facing 3 warnings will have to face game loss.</li>
		</ul>

		<h3>Game playing rules</h3>
		<ul>
		<li>Game will start with a coin toss. The team which wins the toss will choose only one of the two:
			<ul>
			<li> a. Choose Sentinel/Scourge side of the map to start
			<li> b. Choose the first hero.
			</ul></li>
		<li>The game will be played in &ldquo;&mdash; cm&rdquo; mode.</li>
		<li>The game will be saved at regular intervals and at the end as well.</li>
		<li>Warkeys/Dotakeys are allowed.</li>
		</ul>

		<h3>Abuses and Exploitation (Warnings will be given)</h3>
		<ul>
		<li>Trees Trapping: Trapping enemies in trees intentionally is not allowed.</li>
		<li>Pooling: No pooling of any sort is allowed. This means that items bought with a player's own money cannot at any time be carried by any of his teammates. Items bought with the player's own money can only be carried or sold by him. Players can pick up/destroy items dropped by their opponents.</li>
		<li>Pause/Resume Abuse:  Pausing without any valid reason is disallowed. Resuming the game without the other team confirming that they are ready is not allowed either. If someone pauses the game without a valid reason will receive a warning.</li>
		<li>Creep Blocking: Creep blocking is not allowed but creep slowing is allowed. Blocking here refers to the prolonged blocking of creeps to disrupt their usual path. (i.e. Using Chen's creeps to block the entire entrance of the base for a long time.) Creep slowing means that you can stop the creeps from moving in their usual path for a few seconds. (i.e. Using fissure)</li>
		<li>Bugs Exploitation (Game Loss): No Exploitation of any known old or new bug discovered during play. Explanations like "exploiting/activating the bug unknowingly", "we had no idea this bug existed" or similar excuses will not be entertained. Participants/Teams are expected to know what these are. Definition of bug:
		 <ul>
		 <li>Anything abnormal which will give a player/team an unfair advantage.</li>
		 <li>Anything which will cause interruption/fatal error to the game.</li>
		 </ul></li>
		<li>No hero control sharing.</li>
		</ul>
