	<h2>Event Structure</h2>
	<div class="pane">
		<p><em>There will be three phases: </em></p>
                            
		<h3>Phase 1</h3>
		<p> The participants would be required to submit a Boolean expression satisfying the given problem statement.  They need to submit a small write up explaining how they arrived at the expression.</p>

		<h3>Phase 2</h3>
		<p>The co-coordinators will read the abstract and approve the idea that is to be worked upon. This is basically done so that a group can know that they are proceeding in the right direction. Teams would be allowed to participate in the final event only if their abstracts are found satisfactory (according to the rules given). </p>

				
		<h3>Working Model Phase</h3>
		<p>The selected participants are expected to prepare a working model for their expression.  They would be given a time period of 5 days for the same. Each team would be given a desk to showcase their models on the final day. All teams would explain their respective models to the judges verbally.</p>
	</div>
	
	<h2>Rules</h2>
	<div class="pane">
		<ul>
		<li>Students from any department/year can participate because only basic information about Boolean algebra is needed.</li>
		<li>The team should not consist of more than 2 members.</li>
		<li>For example, if the Boolean expression which turns out from the problem is AB’ then the participants have to make its corresponding physical working model without the use of electricity or any kind of fuel.</li>
		<li>Inputs and outputs should have the same representations in the working model. </li>
		<li>Every team would have to submit a description of their model along with the realized Boolean expression to the coordinators, before actually implementing the idea. They would also have to provide their team name, member’s names, group leader name, model name, institution, resources and materials used.</li>
		<li>Any extra Resources like water supply, metal etc. or something that is really needed in advance would be provided only when notified well in advance.</li>
