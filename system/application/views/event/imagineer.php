	<h2>Event Structure</h2>
	<div class="pane">
		<p>Divided into 4 categories</p>
		<ol>
		<li>Cine smArt</li>
		<li>Ad Maestro</li>
		<li>Avatar</li>
		<li>Live the non-living</li>
		</ol>

		<h3>Cine smArt</h3>
		<p>In this category, you can make either a trailer or a parody video.</p>
		<h4>Trailer</h4>
		<p>Those big-budget movies require an orchestra and hours at an editing bay, but if you have got a creative mind and good editing software, make a movie trailer that'll advertise the movie and expose it to a wider audience!!!
		You can make a trailer of any movie of your choice from Bollywood or Hollywood. You can also make a trailer based on a story of your own.</p>
		<h4>Parody</h4>
		<p>Do you see fun in every movies you watch?
		Can you bring along your sense of humor? 
		If you think you can, all you need is to decide on a movie or song or any object, design your plot, decide on your characters, and create your parody!!!
		You can make a parody video of some scenes from a movie. You can also make a parody video of some song.   
		In both of these, videos could be animated also.</p>

		<h3>Ad Maestro</h3>
		<p>Try to sharpen your marketing skills and challenge the existing Ad gurus. This category involves the creation of a video Advertisement. There is no specific theme for the competition and you are required to select your own product. Videos can be animated as well as real. </p>

		<h3>Avatar</h3>
		<p>
		Start scratching your head to bring out the avatar in you..
		</p>
		<p>
		You are required to develop a 2D/3D movie using the latest multimedia  tools. 
		The participant is allowed to choose any theme of his choice, but the movie has to reflect upon the theme. 
		</p>

		<h3>Live The Non-living</h3>

		<p>
		This event requires making of Non-graphical videos .This will include video making of non-living /inanimate objects only. For eg. You can make a movie between a pen and a pencil where they are characters of the movie. Stop motion animation would also come under this category.
		</p>

		<p>In case you are having difficulty understanding this category, a sample movie by the name "sindbad_IMAGINEER sample movie" has been uploaded on college LAN and on <a href="http://in.youtube.com/watch?v=UEaWy1_Hy2I">Youtube</a> with the same name. We hope this sample movie would help you to get a general idea about this category.</p>
