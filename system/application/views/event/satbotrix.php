<h2>Robo-Opus Zonal Guidelines</h2>
<ol>
<li>You have to make a line following autonomous bot.</li>
<li>The colour of the track will be black over white background.</li>
<li>There are no acute angled curves.</li>
<li>There will be breakage in the path which will be marked by a perpendicular intersecting line at the point of the breakage as shown below. When bot encounters such a point it should continue to travel straight. 
<img src="<?=base_url();?>images/satbotrixpath.png" width="150"></img>
</li>
<li>Starting and ending points are same.</li>
<li>Thickness of the line will be  1&rdquo; with 10% error margin.</li>
<li>There will be some check points, each marking different kind of ability. For every check point there will be separate points. More checkpoints you cover, more points you get.</li>
<li>Points will be cut if any wheel except at 90 degrees intersection touches the line.</li>
<li>Maximum two chances will be given to the participants with deduction of points for second try.
Once in the arena you will not be allowed to touch the bot. If you touch the bot while in the arena, it will be considered as a attempt to retry and accordingly rules will be applied.</li>
<li>At any time the potential difference between any two points of the bot shall not be above 18 V.</li>
<li>Motors can have max 200rpm.</li>
<li>Team can consist of max 5 people.</li>
<li>Apart from path speed, other factors like algorithm and innovation in design of bot will be given good weightage. Only propagating through the path is not enough.</li>
</ol>

